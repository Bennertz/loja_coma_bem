package loja_coma_bem;

public class index {
	public static void main(String[] args) {
        int xpto; // declaração de uma variável chamada ‘xpto’
        /*como o valor não foi declarado, por padrão é zero.
        Veremos os tipos de dados a seguir, siga adiante.
        */
		int decimal = 10;

		//declara a variável do tipo int com valor igual a 10
		System.out.println(decimal);

		//mostra o valor da variável int
		long inteiroLongo = 10L;

		//declara a variável do tipo long com valor igual a 10
		//o L simboliza que o valor é de uma variável do tipo long
		System.out.println(inteiroLongo);

		//mostra o valor da variável long
	}
}